package streams.desafios;

public class CarroDesafioFilter {

	final String nome;
	final double preco;
	final boolean arCondicionado;

	public CarroDesafioFilter(String nome, double preco, boolean arCondicionado) {
		this.nome = nome;
		this.preco = preco;
		this.arCondicionado = arCondicionado;
	}

}
